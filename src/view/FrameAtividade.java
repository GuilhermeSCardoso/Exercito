        codigoSoldado = ftfCodigoSoldadoAtividade.getText();
        
        try {
            rs = BuscaAtividade.localizarAdvertencias(codigoSoldado);
        } catch (SQLException ex) {
            Logger.getLogger(FrameAtividade.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            while(rs.next()) {
                tfNumeroAdvertencias.setText(rs.getString("total"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrameAtividade.class.getName()).log(Level.SEVERE, null, ex);
        }
        tfNumeroAdvertencias.setEditable(false);
