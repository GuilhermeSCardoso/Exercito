    private void btSalvarAtividadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarAtividadeActionPerformed
        Atividade atividade = new Atividade();
        
        atividade.setCodigo(Integer.parseInt(ftfCodigoAtividade.getText()));
        atividade.setMes(cbMes.getSelectedItem().toString());
        atividade.setAulasTeoricas(chkAulasTeoricas.isSelected());
        atividade.setFlexoes(chkFlexoes.isSelected());
        atividade.setAbdominais(chkAbdominais.isSelected());
        atividade.setNatacao(chkNatacao.isSelected());
        atividade.setCorrida(chkCorrida.isSelected());
        atividade.setDefesaPessoal(chkDefesaPessoal.isSelected());
        atividade.setRastejarLama(chkRastejarLama.isSelected());
        atividade.setPraticaTiro(chkPraticaTiro.isSelected());
        atividade.setCodigoSoldado(Integer.parseInt(ftfCodigoSoldadoAtividade.getText()));
        atividade.setNomeSoldado(tfNomeSoldadoAtividade.getText());
        
        try {
            GravaAtividade.cadastrarNovaAtividade(atividade);
        } catch (SQLException ex) {
            Logger.getLogger(FrameAtividade.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(chkAulasTeoricas.isSelected() == true &&
           chkFlexoes.isSelected() == true &&
           chkAbdominais.isSelected() == true &&
           chkNatacao.isSelected() == true &&
           chkCorrida.isSelected() == true &&
           chkDefesaPessoal.isSelected() == true &&
           chkRastejarLama.isSelected() == true &&
           chkPraticaTiro.isSelected() == true) {
                dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Atividade Mensal Incompleta!", "ADVERTÊNCIA", JOptionPane.WARNING_MESSAGE);
            String codigoSoldado;
            int adv;
            codigoSoldado = ftfCodigoSoldadoAtividade.getText();
            adv = Integer.parseInt(tfNumeroAdvertencias.getText());
            adv = adv + 1;
            try {
                GravaAtividade.contarAdvertencia(codigoSoldado, adv);
            } catch (SQLException ex) {
                Logger.getLogger(FrameAtividade.class.getName()).log(Level.SEVERE, null, ex);
            }
            dispose();
        }
                
    }//GEN-LAST:event_btSalvarAtividadeActionPerformed

    private void btBuscarSoldadoAtividadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarSoldadoAtividadeActionPerformed
        String codigoSoldado;
        ResultSet rs = null;
        
        codigoSoldado = ftfCodigoSoldadoAtividade.getText();
        
        try {
            rs = BuscaAtividade.localizarSoldado(codigoSoldado);
        } catch (SQLException ex) {
            Logger.getLogger(FrameAtividade.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {    
            while(rs.next()) {
                tfNomeSoldadoAtividade.setText(rs.getString("nome"));
                tfCasernaSoldadoAtividade.setText(rs.getString("caserna"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrameAtividade.class.getName()).log(Level.SEVERE, null, ex);
        }
        ftfCodigoSoldadoAtividade.setEditable(false);
        tfNomeSoldadoAtividade.setEditable(false);
        tfCasernaSoldadoAtividade.setEditable(false);
        btNumeroAdvertencias.setEnabled(true);
    }//GEN-LAST:event_btBuscarSoldadoAtividadeActionPerformed

